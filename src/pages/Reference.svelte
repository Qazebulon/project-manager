  <h2>Purpose</h2>
  <p>
    Find a consistent approach to go from idea to deployed proof of concept in
    as short a cycle as possible while maintaining reasonable coding standards.
  </p>

  <h2>Methodology</h2>
  <ol>
    <li>
      Starting with cycle times of one Month, the goal is to reduce the cycles
      times as far as possible.
    </li>
    <li>
      Alternate between working on a project and then working on improving
      tooling / approach.
    </li>
  </ol>

  <h2>Checklist: Generating An Initial Idea -> Queued Idea</h2>
  <ol>
    <li>Inspiration strikes (new idea)</li>
    <li>
      Create an entry in Obsidian for the new idea to capture the 'word vomit'
      version of the idea...
    </li>
    <li>Edit the idea into a clear concept.</li>
    <li>
      Review the potential merits of the idea... move to discarded, backlog,
      queued.
    </li>
  </ol>

  <h2>Checklist: Executing A Queued Idea</h2>
  <ol>
    <li>Move the Queued Idea into Current</li>
    <li>Create a project folder and local repo</li>
    <li>
      Update current project command (Alias c) to point to new project folder
    </li>
    <li>Do an initial spike</li>
    <li>Push the local repo to Github</li>
    <li>Create a Deployed branch</li>
    <li>
      Create new AWS Amplify project to deploy the Deployed branch (Considering
      switching out for CloudFormation/Terraform/SAML)
    </li>
    <li>
      Create a Github Project
      <ol>
        <li>Add columns with automation</li>
        <li>Create a milestone for each phase</li>
        <li>Create issues for the current phase</li>
      </ol>
    </li>
    <li>Create a Github Project</li>
    <li>
      Iterate on steps in the Project until all steps are deployed and tested
      (or shelved) for a phase
    </li>
    <li>
      Update the Project with information for the next phase and repeat the
      iteration step above...
    </li>
  </ol>

